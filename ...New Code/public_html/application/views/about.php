          <h4>Team Institute</h4>
          <p>Our Team Institute incorporates the power of a positive peer group. This Institute uses competition and team work to teach the students how to work together to accomplish a common goal, as well as the value of friendship. Each team is captained by a fellow student and competes by acquiring points through participation in any of the BETTER ME BETTER TOMORROW (B.M.B.T.) Activities. At the end of each season (6 months) the team that has accumulated the most points wins. The prize, a 3 day trip of some value.</p>
          <br>
          <br>

          <img src="img/whatWeDo/3.jpg" width="100" height="100"/>
          <h4>Church Institute</h4>
          <p>Our Church Institute engages the local community of churches with a larger number of youth and helps build a relationship between a very un-churched and disenfranchised generation with a loving and sincere local body of believers. By creating an environment that helps foster relationships between local youth ministers and youth leaders with students of similar interest and needs, we believe we can effectively help each student develop a long term relationship with a local church. </p>
          <br>
          <br>

          <img src="img/whatWeDo/4.jpg" width="100" height="100"/>
          <h4>Skills Institute</h4>
          <p>Our Skills Institute provides the tools and equipment necessary to allow students to pursue and perfect their skills no matter what their passions. Skateboarding, basketball, weightlifting, creative arts, dance, automotive, radio, and many more. In each Skill, there will be a leader, not only to help them develop their skill but to learn about themselves in the process. Character, ethics, morality, honesty, perseverance, and hard work are taught and expected on a daily basis.</p>
